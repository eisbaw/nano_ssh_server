int main(void) {
    int passed = 0;
    int failed = 0;
    int total = sizeof(test_vectors) / sizeof(test_vectors[0]);

    printf("=== HMAC-SHA256 Test Vectors ===\n");
    printf("Running %d test vectors...\n\n", total);

    for (int i = 0; i < total; i++) {
        const hmac_test_vector_t *tv = &test_vectors[i];
        uint8_t mac[32];
        hmac_sha256_ctx ctx;

        /* Compute HMAC-SHA256 */
        hmac_sha256_init(&ctx, tv->key, tv->key_len);
        hmac_sha256_update(&ctx, (const uint8_t *)tv->data, tv->data_len);
        hmac_sha256_final(&ctx, mac);

        /* Verify */
        if (memcmp(mac, tv->expected, 32) == 0) {
            printf("[PASS] %s\n", tv->name);
            passed++;
        } else {
            printf("[FAIL] %s\n", tv->name);
            printf("  Key length: %zu, Data length: %zu\n", 
                   tv->key_len, tv->data_len);
            printf("  Expected: ");
            for (int j = 0; j < 32; j++) {
                printf("%02x", tv->expected[j]);
            }
            printf("\n  Got:      ");
            for (int j = 0; j < 32; j++) {
                printf("%02x", mac[j]);
            }
            printf("\n");
            failed++;
        }
    }

    printf("\n=============================\n");
    printf("Results: %d passed, %d failed out of %d tests\n", 
           passed, failed, total);
    printf("=============================\n");

    return (failed == 0) ? 0 : 1;
}
