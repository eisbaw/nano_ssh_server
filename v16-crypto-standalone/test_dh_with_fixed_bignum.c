int main() {
    uint8_t private_key[256], public_key[256];
    bn_t priv, pub, prime, generator;

    printf("Testing DH key generation with fixed bignum\n\n");

    /* Generate random private key */
    if (random_bytes(private_key, 256) < 0) {
        printf("Failed to generate random bytes\n");
        return 1;
    }

    /* Ensure private key < prime */
    private_key[0] &= 0x7F;

    printf("Generated random private key (first 16 bytes):\n ");
    for (int i = 0; i < 16; i++) {
        printf(" %02x", private_key[i]);
    }
    printf("\n\n");

    /* Setup */
    bn_from_bytes(&priv, private_key, 256);
    bn_from_bytes(&prime, dh_group14_prime, 256);
    bn_zero(&generator);
    generator.array[0] = 2;

    printf("Computing public key: g^priv mod prime...\n");
    printf("This will take a moment (2048-bit modexp)...\n\n");

    /* Compute public = 2^private mod prime */
    bn_modexp(&pub, &generator, &priv, &prime);

    /* Check result */
    if (bn_is_zero(&pub)) {
        printf("❌ FAIL: Public key is zero!\n");
        return 1;
    }

    /* Check if public key > 1 */
    bn_t one;
    bn_zero(&one);
    one.array[0] = 1;

    if (bn_cmp(&pub, &one) <= 0) {
        printf("❌ FAIL: Public key <= 1\n");
        return 1;
    }

    /* Convert to bytes */
    bn_to_bytes(&pub, public_key, 256);

    printf("✅ SUCCESS: Generated valid public key!\n\n");
    printf("Public key (first 32 bytes):\n  ");
    for (int i = 0; i < 32; i++) {
        printf("%02x", public_key[i]);
    }
    printf("\n\n");

    printf("Public key (last 32 bytes):\n  ");
    for (int i = 224; i < 256; i++) {
        printf("%02x", public_key[i]);
    }
    printf("\n\n");

    /* Verify it's not all zeros or garbage */
    int has_content = 0;
    for (int i = 0; i < 256; i++) {
        if (public_key[i] != 0) {
            has_content = 1;
            break;
        }
    }

    if (!has_content) {
        printf("❌ FAIL: Public key is all zeros\n");
        return 1;
    }

    printf("✅ Public key validation passed!\n");
    printf("✅ Fixed bignum implementation works for DH!\n");

    return 0;
}
