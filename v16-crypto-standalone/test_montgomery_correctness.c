static int random_bytes(uint8_t *buf, size_t len) {
    int fd = open("/dev/urandom", O_RDONLY);
    if (fd < 0) return -1;
    ssize_t n = read(fd, buf, len);
    close(fd);
    return (n == (ssize_t)len) ? 0 : -1;
}

int main() {
    uint8_t private_key[256];
    uint8_t result_mont[256], result_binary[256];

    printf("========================================\n");
    printf("Montgomery vs Binary Division Correctness Test\n");
    printf("========================================\n\n");

    /* Generate random private key */
    if (random_bytes(private_key, 256) < 0) {
        fprintf(stderr, "Failed to generate random bytes\n");
        return 1;
    }
    private_key[0] &= 0x7F;

    /* Test 1: Compute with Montgomery */
    {
        bn_t_mont priv, pub, prime, generator;
        bn_from_bytes_mont(&priv, private_key, 256);
        bn_from_bytes_mont(&prime, dh_group14_prime, 256);
        bn_zero(&generator);
        generator.array[0] = 2;

        bn_modexp_mont(&pub, &generator, &priv, &prime);
        bn_to_bytes_mont(&pub, result_mont, 256);
    }

    /* Test 2: Compute with binary division */
    {
        bn_t priv, pub, prime, generator;
        bn_from_bytes(&priv, private_key, 256);
        bn_from_bytes(&prime, dh_group14_prime, 256);
        bn_zero(&generator);
        generator.array[0] = 2;

        bn_modexp(&pub, &generator, &priv, &prime);
        bn_to_bytes(&pub, result_binary, 256);
    }

    /* Compare results */
    printf("Comparing results...\n");
    int identical = 1;
    for (int i = 0; i < 256; i++) {
        if (result_mont[i] != result_binary[i]) {
            if (identical) {
                printf("❌ DIFFERENCE FOUND:\n");
                identical = 0;
            }
            printf("  Byte %3d: Montgomery=0x%02x, Binary=0x%02x\n",
                   i, result_mont[i], result_binary[i]);
        }
    }

    if (identical) {
        printf("✅ IDENTICAL: Both methods produce the same result!\n");
        printf("\nFirst 32 bytes of result:\n");
        for (int i = 0; i < 32; i++) {
            printf("%02x", result_mont[i]);
            if ((i + 1) % 16 == 0) printf("\n");
        }
        printf("...\n");
    } else {
        printf("\n❌ FAIL: Methods produce different results!\n");
        printf("This indicates a bug in Montgomery multiplication.\n");
        return 1;
    }

    return 0;
}
