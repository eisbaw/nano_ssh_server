/* Generator g = 2 */
#define DH_GROUP14_GENERATOR 2

/* DH context */
typedef struct {
    bigint_t prime;
    bigint_t generator;
    bigint_t private_key;
    bigint_t public_key;
} dh_context_t;

/* Initialize DH context */
static void dh_init(dh_context_t *ctx) {
    bigint_from_bytes(&ctx->prime, dh_group14_prime, sizeof(dh_group14_prime));
    bigint_set_u64(&ctx->generator, DH_GROUP14_GENERATOR);
}

/* Generate DH keypair */
static void dh_generate_keypair(dh_context_t *ctx) {
    /* Generate random private key (256 bits for efficiency) */
    uint8_t private_bytes[32];
    randombytes(private_bytes, 32);
    bigint_from_bytes(&ctx->private_key, private_bytes, 32);

    /* Compute public key: g^private mod p */
    bigint_modexp(&ctx->public_key, &ctx->generator, &ctx->private_key, &ctx->prime);
}

/* Compute shared secret: peer_public^private mod p */
static void dh_compute_shared(bigint_t *shared, const dh_context_t *ctx, const bigint_t *peer_public) {
    bigint_modexp(shared, peer_public, &ctx->private_key, &ctx->prime);
}

/* Export public key to bytes */
static void dh_export_public(uint8_t *out, int len, const dh_context_t *ctx) {
    bigint_to_bytes(out, len, &ctx->public_key);
}

/* Import peer public key from bytes */
static void dh_import_peer_public(bigint_t *peer_public, const uint8_t *bytes, int len) {
    bigint_from_bytes(peer_public, bytes, len);
}

