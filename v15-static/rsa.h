/* Public exponent (e = 65537) */
#define RSA_PUBLIC_EXPONENT 65537

/*
 * Initialize RSA key with hardcoded values
 */
static inline void rsa_init_key(rsa_key_t *key) {
    bn_from_bytes(&key->n, rsa_modulus, 256);
    bn_from_bytes(&key->d, rsa_private_exponent, 256);

    /* e = 65537 */
    bn_zero(&key->e);
    key->e.array[0] = RSA_PUBLIC_EXPONENT;
}

/*
 * PKCS#1 v1.5 padding for SHA-256 signatures
 *
 * Format: 0x00 || 0x01 || PS || 0x00 || DigestInfo || hash
 *
 * DigestInfo for SHA-256:
 *   30 31 30 0d 06 09 60 86 48 01 65 03 04 02 01 05 00 04 20
 *   (19 bytes)
 *
 * For 2048-bit (256 bytes):
 *   - 0x00 0x01: 2 bytes
 *   - PS (0xFF padding): 256 - 3 - 19 - 32 = 202 bytes
 *   - 0x00: 1 byte
 *   - DigestInfo: 19 bytes
 *   - hash: 32 bytes
 */
static inline void rsa_pkcs1_pad_sha256(uint8_t *padded, const uint8_t *hash) {
    /* DigestInfo for SHA-256 */
    static const uint8_t digest_info[19] = {
        0x30, 0x31, 0x30, 0x0d, 0x06, 0x09, 0x60, 0x86,
        0x48, 0x01, 0x65, 0x03, 0x04, 0x02, 0x01, 0x05,
        0x00, 0x04, 0x20
    };

    /* 0x00 0x01 */
    padded[0] = 0x00;
    padded[1] = 0x01;

    /* PS (0xFF padding) */
    memset(padded + 2, 0xFF, 202);

    /* 0x00 separator */
    padded[204] = 0x00;

    /* DigestInfo */
    memcpy(padded + 205, digest_info, 19);

    /* Hash (32 bytes) */
    memcpy(padded + 224, hash, 32);
}

/*
 * RSA sign operation
 *
 * signature = (padded_message)^d mod n
 *
 * message: 32-byte SHA-256 hash
 * signature: 256-byte RSA signature
 */
static inline int rsa_sign(uint8_t *signature, const uint8_t *message, const rsa_key_t *key) {
    uint8_t padded[256];
    bn_t padded_bn, sig_bn;

    /* Apply PKCS#1 v1.5 padding */
    rsa_pkcs1_pad_sha256(padded, message);

    /* Convert to bignum */
    bn_from_bytes(&padded_bn, padded, 256);

    /* signature = padded^d mod n */
    bn_modexp(&sig_bn, &padded_bn, &key->d, &key->n);

    /* Export signature */
    bn_to_bytes(&sig_bn, signature, 256);

    return 0;
}

/*
 * RSA verify operation (for testing)
 *
 * padded = signature^e mod n
 * Then verify padding and extract hash
 */
static inline int rsa_verify(const uint8_t *signature, const uint8_t *message, const rsa_key_t *key) {
    uint8_t padded[256];
    uint8_t expected_padded[256];
    bn_t sig_bn, padded_bn;

    /* Convert signature to bignum */
    bn_from_bytes(&sig_bn, signature, 256);

    /* padded = signature^e mod n */
    bn_modexp(&padded_bn, &sig_bn, &key->e, &key->n);

    /* Export padded message */
    bn_to_bytes(&padded_bn, padded, 256);

    /* Create expected padded message */
    rsa_pkcs1_pad_sha256(expected_padded, message);

    /* Compare */
    if (memcmp(padded, expected_padded, 256) == 0) {
        return 0;  /* Verification success */
    } else {
        return -1;  /* Verification failed */
    }
}

/*
 * Export RSA public key in SSH format
 *
 * SSH RSA public key format:
 *   string "ssh-rsa"
 *   mpint  e (public exponent)
 *   mpint  n (modulus)
 */
static inline int rsa_export_public_key_ssh(uint8_t *output, size_t *output_len, const rsa_key_t *key) {
    uint8_t *p = output;
    uint8_t n_bytes[256];

    /* Export modulus */
    bn_to_bytes(&key->n, n_bytes, 256);

    /* String "ssh-rsa" (4-byte length + data) */
    *p++ = 0x00; *p++ = 0x00; *p++ = 0x00; *p++ = 0x07;
    memcpy(p, "ssh-rsa", 7);
    p += 7;

    /* mpint e (public exponent = 65537 = 0x010001) */
    *p++ = 0x00; *p++ = 0x00; *p++ = 0x00; *p++ = 0x03;
    *p++ = 0x01; *p++ = 0x00; *p++ = 0x01;

    /* mpint n (modulus - 256 bytes, add 0x00 if high bit set) */
    if (n_bytes[0] & 0x80) {
        /* Need leading zero for mpint */
        *p++ = 0x00; *p++ = 0x00; *p++ = 0x01; *p++ = 0x01;
        *p++ = 0x00;
        memcpy(p, n_bytes, 256);
        p += 256;
    } else {
        *p++ = 0x00; *p++ = 0x00; *p++ = 0x01; *p++ = 0x00;
        memcpy(p, n_bytes, 256);
        p += 256;
    }

    *output_len = p - output;
    return 0;
}

